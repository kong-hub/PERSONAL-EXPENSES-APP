          Column(
            children: transactions.map((transaction) {
              return Card(
                  child: Row(
                children: [
                  Container(
                    margin: EdgeInsets.symmetric(
                      vertical: 10,
                      horizontal: 15,
                    ),
                    decoration: BoxDecoration(
                      border: Border.all(
                        color: Colors.black,
                        width: 2,
                      ),
                    ),
                    padding: EdgeInsets.all(10),
                    child: Text(
                      transaction.amount.toString(),
                    ),
                  ),
                  Column(
                    children: [
                      Text(
                        transaction.title,
                      ),
                      Text(
                        transaction.date.toString(),
                      )
                    ],
                  )
                ], //Text(transaction.title),
              ));
            }).toList(),
